import { chromium } from "playwright";
const SHOT = process.env.SHOT_DIR ?? ".";
const stamp = Date.now().toString().slice(-6);
const browser = await chromium.launch({ channel: "msedge" });
const page = await browser.newPage({ viewport: { width: 1400, height: 1100 } });
await page.goto("http://localhost:3000/member/register", { waitUntil: "networkidle" });
await page.waitForTimeout(2000);
await page.fill("#firstname", "Head" + stamp.slice(0, 3));
await page.selectOption("#gender", "female");
await page.fill("#date_of_birth", "1991-05-05");
await page.fill("#mobile_no", "024" + stamp + "9");
await page.fill("#email", `head${stamp}@example.com`);
await page.fill("#password", "HouseUi#2026");
await page.fill("#password_confirmation", "HouseUi#2026");
// add two household members
await page.getByRole("button", { name: "+ Add Member" }).click();
await page.getByRole("button", { name: "+ Add Member" }).click();
const cards = page.locator("form .rounded-sm.border.border-warm-deep.p-4");
const first = cards.nth(0);
await first.getByPlaceholder("First name *").fill("Kojo");
await first.getByTitle("Relationship").selectOption("partner");
await first.getByTitle("Gender").selectOption("male");
await first.locator('input[type=date]').fill("1989-02-11");
const second = cards.nth(1);
await second.getByPlaceholder("First name *").fill("Abena");
await second.getByTitle("Relationship").selectOption("child");
await second.getByTitle("Gender").selectOption("female");
await second.locator('input[type=date]').fill("2018-07-07");
await page.screenshot({ path: `${SHOT}/register-household.png`, fullPage: true });
await page.getByRole("button", { name: /Create Account/i }).click();
await page.waitForURL(/\/member$/, { timeout: 30000 });
console.log("registered + auto-logged-in: YES");
console.log("EMAIL:", `head${stamp}@example.com`);
await browser.close();
